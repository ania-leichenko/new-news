import { useState, useEffect } from "react";
import Head from "next/head";
import Header from "@/components/Header/Header";
import { useRouter } from "next/router";
import { client } from "api/axios";
import { NewsItem } from "@/types";
import { makeStyles } from "@material-ui/core/styles";
import Button from "@material-ui/core/Button";
import TextField from "@material-ui/core/TextField";
import CardHeader from '@material-ui/core/CardHeader';
import Avatar from '@material-ui/core/Avatar';
import Card from '@material-ui/core/Card';

const useStyles = makeStyles((theme) => ({
  root: {
    maxWidth: 345,
    textAlign: "center",
    border: "1px solid black",
    justifyContent: "center",
    margin: theme.spacing(1),
  },
  main: {
    textAlign: "center",
  },
  description: {
    fontSize: "20px",
  },
  footer: {
    textAlign: "center",
    marginTop: "50px",
  },
  add: {
    marginLeft: "8px",
  },
  comment: {
    width: "500px",
  },
}));

export default function Home() {
  const classes = useStyles();
  const router = useRouter();
  let id = router.query.id;
  let [item, setItem] = useState<NewsItem>();
  let [comment, setComment] = useState("");

  function clickHandler() {
    client
      .post("/api/comments", {
        comment,
        id,
      })
      .catch(() => {});
  }
  const value = item?.comments.map((comment) => (
    <p key={comment._id}>
      {comment.value}
    </p>
  ));
  const userEmail = item?.comments.map((comment) => (
    <p key={comment._id}>
      {comment.userEmail}
    </p>
  ));
  const userImage = item?.comments.map((comment) => (
    <p key={comment._id}>
      <Avatar alt="Remy Sharp" src={comment.userImage} />
    </p>
  ));
 
  useEffect(() => {
    client.get(`/api/completenew?id=${id}`).then((result) => {
      setItem(result.data);
    });
  }, [id]);

  return (
    <div>
      <Head>
        <title>new-news</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Header />
      {item ? (
        <main className={classes.main}>
          <img src={item?.image} />
          <h1>{item?.title}</h1>
          <div className={classes.description}>
            <p>{item?.description}</p>
          </div>
          <footer className={classes.footer}>
            <TextField
              className={classes.comment}
              placeholder="Коментарии"
              onChange={function (e) {
                setComment(e.target.value);
              }}
            />
            <Button
              className={classes.add}
              variant="contained"
              color="primary"
              onClick={clickHandler}
            >
              Добавить
            </Button>
            {item.comments.map((comment) => (
            <Card key={comment._id} className={classes.root}>
              <CardHeader
                avatar={
                  <Avatar alt="Remy Sharp" src={comment.userImage} />
                }
                title={comment.userEmail}
                subheader={comment.value}
              />
            </Card>
            ))}
          </footer>
        </main>
      ) : (
        <main className={classes.main}>Новость не найдена</main>
      )}
    </div>
  );
}
